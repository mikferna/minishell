#include "../../includes/minishell.h"

char	*ft_strdup(const char *s1)
{
	char			*p;
	unsigned int	i;
	unsigned int	j;

	j = ft_strlen(s1);
	p = malloc(j + 1);
	if (p == 0)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
